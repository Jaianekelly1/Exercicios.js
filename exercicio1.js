// 01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
// multiplicação e divisão desses valores

function soma(a , b) {
    return a + b
}
console.log(soma(3, 2))


function sub(a, b) {
    return a - b
}
console.log(sub(4, 3))


function mult(a, b) {
    return a * b
}
console.log(mult(5, 25))

function div(a, b) {
    return a / b
}
console.log(div(8, 2))

